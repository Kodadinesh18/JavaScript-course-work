//intro loops

//for loops
//start index,end condition,increment
for(let i=1;i<=10;i++){
    console.log(i)
}

//while loops
//initialize outside loop, add condition,increment
let i=1
while(i<=10){
    console.log(i)
    i++;
}

//do while loops:
//firt-execute the code and after that checks for condition
let j=1
do{
    console.log("hi")
    console.log(j)
    j++;
}while(j<2);